function describeCountry(country, population, capitalCity){
    return `${country} has ${population} and it's capital is ${capitalCity}`;
}

let finland = describeCountry('Finland','6 Million' ,'Helsinki')
let israel = describeCountry('Israel','9 Million' ,'Jeruslaem')
let france = describeCountry('France','80 Million' ,'Paris')
console.log(israel);
console.log(france);
console.log(finland);

