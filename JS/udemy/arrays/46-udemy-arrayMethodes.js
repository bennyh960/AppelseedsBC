// push - add to end (like append in python)
// pop - remove from and (same as python)
// shift - remove from start
// unshift - add to start

let arr = [10,20,30];

arr.push(50);
console.log(arr)

arr.pop();
console.log(arr)

arr.shift()
console.log(arr)

arr.unshift(1)
console.log(arr)

console.log(arr.push(15)); //print the length
console.log(arr)
console.log(arr.pop()); //print the last index item
console.log(arr.shift()); //print the first index item
console.log(arr.unshift(9)); //print the length
console.log(arr)


